package com.bean;

public class Container {

	int cont_id;
	String cont_cliente;
	String cont_num;
	int cont_tipo;
	String cont_status;
	String cont_categoria;

	public int getCont_id() {
		return cont_id;
	}

	public void setCont_id(int cont_id) {
		this.cont_id = cont_id;
	}

	public String getCont_cliente() {
		return cont_cliente;
	}

	public void setCont_cliente(String cont_cliente) {
		this.cont_cliente = cont_cliente;
	}

	public String getCont_num() {
		return cont_num;
	}

	public void setCont_num(String cont_num) {
		this.cont_num = cont_num;
	}

	public int getCont_tipo() {
		return cont_tipo;
	}

	public void setCont_tipo(int cont_tipo) {
		this.cont_tipo = cont_tipo;
	}

	public String getCont_status() {
		return cont_status;
	}

	public void setCont_status(String cont_status) {
		this.cont_status = cont_status;
	}

	public String getCont_categoria() {
		return cont_categoria;
	}

	public void setCont_categoria(String cont_categoria) {
		this.cont_categoria = cont_categoria;
	}

}
